    private void removerPorNrQuartoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removerPorNrQuartoActionPerformed
        try{    
            if(comboBoxNrQuarto.getSelectedItem() != null){
            Connection con = DriverManager.getConnection("jdbc:derby://localhost:1527/Hotel", "root", "123456" );
            
            String sql = "UPDATE quartos SET ocupado=false, cliente=NULL, data_inicial=NULL, data_final=NULL WHERE numero=?";
            PreparedStatement pstmt = con.prepareStatement(sql);           
            pstmt.setString(1, comboBoxNrQuarto.getSelectedItem().toString());
            pstmt.executeUpdate();
            
            con.close();
            
            JOptionPane.showMessageDialog(this, "Reserva cancelada!", "", JOptionPane.INFORMATION_MESSAGE);
            //DISPOSE WINDOW
            parent.setEnabled(true);
            parent.toFront();
            this.dispose();
            }
            else{
                JOptionPane.showMessageDialog(this, "Selecione os dados da lista", "Erro", JOptionPane.ERROR_MESSAGE);                   
            }
        }
        catch(SQLException ex){
            
        }
    }//GEN-LAST:event_removerPorNrQuartoActionPerformed

