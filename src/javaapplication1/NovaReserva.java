    private void buscarDadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarDadosActionPerformed
              
        try{
            Connection con = DriverManager.getConnection("jdbc:derby://localhost:1527/Hotel", "root", "123456" );
            
            PreparedStatement nomesQuery = con.prepareStatement("SELECT * FROM ROOT.clientes");
            PreparedStatement quartosQuery = con.prepareStatement("SELECT numero FROM ROOT.quartos WHERE ocupado = false");
            ResultSet nomesrs = nomesQuery.executeQuery();            
            ResultSet quartosrs = quartosQuery.executeQuery(); 
            
            comboBoxNomes.removeAllItems();
            comboBoxNrQuarto.removeAllItems();
            
            while(nomesrs.next()){
                
                comboBoxNomes.addItem(nomesrs.getString("nome"));
            }
            
            while(quartosrs.next()){
                
                comboBoxNrQuarto.addItem(quartosrs.getString("numero"));
            }
            
            con.close();
        }
        catch(SQLException ex){
            
        }
        
        
    }//GEN-LAST:event_buscarDadosActionPerformed

