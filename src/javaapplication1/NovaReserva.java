    private void efetuarReservaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_efetuarReservaActionPerformed
        //set calendars
        LocalDate iCal = null;
        LocalDate fCal = null;
        try{
            if(dataInicial.getDate() != null && dataFinal.getDate() != null 
               && comboBoxNomes.getSelectedItem() != null && comboBoxNrQuarto.getSelectedItem() !=null){
                
                iCal = dataInicial.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
                fCal = dataFinal.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
                
                if(iCal.isBefore(fCal) && (iCal.isAfter(LocalDate.now()) || iCal.isEqual(LocalDate.now()))){
                    Connection con = DriverManager.getConnection("jdbc:derby://localhost:1527/Hotel", "root", "123456");

                    String sql = "UPDATE quartos SET ocupado=true, cliente=?, data_inicial=?, data_final=? WHERE numero=?";
                    PreparedStatement st = con.prepareStatement(sql);
                    st.setString(1, comboBoxNomes.getSelectedItem().toString());
                    st.setString(2, java.sql.Date.valueOf(iCal).toString());
                    st.setString(3, java.sql.Date.valueOf(fCal).toString());
                    st.setString(4, comboBoxNrQuarto.getSelectedItem().toString());
                    st.executeUpdate();
                    
                    
                    JOptionPane.showMessageDialog(this, "Reserva Efetuada com Sucesso!", "", JOptionPane.INFORMATION_MESSAGE);
                    parent.setEnabled(true);
                    parent.toFront();
                    this.dispose();
                }
                else{
                    JOptionPane.showMessageDialog(this, "Datas incorretas", "Erro", JOptionPane.ERROR_MESSAGE);                    
                }
            }

            else{
                JOptionPane.showMessageDialog(this, "Favor inserir todos os dados corretamente", "Erro", JOptionPane.ERROR_MESSAGE);
            }        
        }
        catch(SQLException ex){
            
        }
        
        
        
        
    }//GEN-LAST:event_efetuarReservaActionPerformed

    

